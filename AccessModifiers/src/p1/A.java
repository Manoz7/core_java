package p1;

public class A {

	private int a = 10;
	int b = 40;
	protected int c = 50;
	public int d = 99;

	public static void main(String[] args) {
		A aobject = new A();
		System.out.println(aobject.a);
		System.out.println(aobject.b);
		System.out.println(aobject.c);
		System.out.println(aobject.d);

	}

}
