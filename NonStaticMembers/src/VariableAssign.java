
public class VariableAssign {
	int num = 10;
	float fd = 20.54f;
	char ch = 'x';
	String str = "I am the best";

	public static void main(String[] args) {

		VariableAssign asgn = new VariableAssign();

		System.out.println(asgn.num);
		System.out.println(asgn.fd);
		System.out.println(asgn.ch);
		System.out.println(asgn.str);

	}

}
